    private function validateRequest()
    {
        return request()->validate([
            'name' => 'required',
            'email' => 'required|email',
            'website' => '',
            'logo' => 'sometimes|file|image|max:5000'
        ]);
    }

    public function storeLogo($company)
    {
        if (request()->has('logo')) {
            $company->update([
                'logo' => request()->logo->store('uploads', 'public')
            ]);
        }
    }
